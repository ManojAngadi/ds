# Integer
a = 10
print("Integer:", a, "Type:", type(a))

# Float
b = 2.5
print("Float:", b, "Type:", type(b))

# Complex
c = 1 + 2j
print("Complex:", c, "Type:", type(c))

# String
d = "Hi"
print("String:", d, "Type:", type(d))

# Boolean
e = True
print("Boolean:", e, "Type:", type(e))

# List
f = [1, "a", True]
print("List:", f, "Type:", type(f))

# Tuple
g = (2, "b", False)
print("Tuple:", g, "Type:", type(g))

# Set
h = {1, 2, 2, "x"}
print("Set:", h, "Type:", type(h))

# Dictionary
i = {"name": "Bob", "age": 20}
print("Dictionary:", i, "Type:", type(i))
